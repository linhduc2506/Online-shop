    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String cateID = request.getParameter("id");

        List<ProductCategory> listC = null;
        List<Product> list = null;
        Product latestProduct = null;
        try {
            listC = ProductCategoryDAO.allCategory();
            list = ProductCategoryDAO.getProductByCategory(cateID);
            latestProduct = ProductDAO.getLatestProductByID();
        } catch (SQLException ex) {
            Logger.getLogger(ProductCategoryController.class.getName()).log(Level.SEVERE, null, ex);
        }

        request.setAttribute("latestProduct", latestProduct);
        request.setAttribute("listC", listC);
        request.setAttribute("listP", list);

        request.getRequestDispatcher("Shop.jsp").forward(request, response);

//        processRequest(request, response);
    }

